    # Attach HTML to alternative container (NOT directly to msg)
    msg_alt.attach(MIMEText(html_content_with_image, "html"))

    # Attach image
    with open("rating_trend.png", "rb") as img:
        mime_img = MIMEImage(img.read())
        mime_img.add_header("Content-ID", "<rating_graph>")
        mime_img.add_header("Content-Disposition", "inline", filename="rating_trend.png")
        msg.attach(mime_img)

    server = smtplib.SMTP_SSL("smtp.gmail.com", 465)
    server.login(sender_email, app_password)
